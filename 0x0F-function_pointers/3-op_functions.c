#include "3-calc.h"
/**
 *op_add - a function that add two argument
 *@a: first int argument
 *@b: second int argument
 *Return: int
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 *op_sub - a function that subtract one argument from another
 *@a: first int argument
 *@b: second int argument
 *Return: int of a-b
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 *op_mod - a function that calculate modulo of two argument
 *@a: first int argument
 *@b: second int argument
 *Return: int of a mod b
 */
int op_mod(int a, int b)
{
return (a % b);
}

/**
 *op_div - a function that divide two argument
 *@a: first argument
 *@b: second argument
 *Return: int of a divide
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 *op_mul - a function that multiply two argument
 *@a: first int argument
 *@b: second int argument
 *Return: int of a multiplied by b
 */
int op_mul(int a, int b)
{
return (a * b);
}
